package org.ghotibeaun.json.exception;

public class JSONUnmarshallingException extends JSONException {

    /**
     * 
     */
    private static final long serialVersionUID = 1666093866365992651L;

    public JSONUnmarshallingException() {
        super();
    }

    public JSONUnmarshallingException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public JSONUnmarshallingException(String arg0) {
        super(arg0);
    }

    public JSONUnmarshallingException(Throwable arg0) {
        super(arg0);
    }



}
